class CountryCode {
  CountryCode({
    this.countries,
  });

  CountryCode.fromJson(dynamic json) {
    if (json['countries'] != null) {
      countries = [];
      json['countries'].forEach((v) {
        countries?.add(Country.fromJson(v));
      });
    }
  }

  List<Country>? countries;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (countries != null) {
      map['countries'] = countries?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}

class Country {
  Country({
    this.id,
    this.countryName,
    this.prefix,
    this.link,
    this.index,
    this.code,
  });

  Country.fromJson(dynamic json) {
    id = json['id'];
    countryName = json['country_name'] != null ? CountryName.fromJson(json['country_name']) : null;
    prefix = json['prefix'];
    link = json['link'];
    index = json['index'];
    code = json['code'];
  }

  num? id;
  int? index;
  CountryName? countryName;
  String? prefix;
  String? link;
  String? code;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['id'] = id;
    if (countryName != null) {
      map['country_name'] = countryName?.toJson();
    }
    map['prefix'] = prefix;
    map['index'] = index;
    map['link'] = link;
    map['code'] = code;
    return map;
  }
}

class CountryName {
  CountryName({
    this.nameRu,
    this.nameUz,
    this.nameEn,
  });

  CountryName.fromJson(dynamic json) {
    nameRu = json['name_ru'];
    nameUz = json['name_uz'];
    nameEn = json['name_en'];
  }

  String? nameRu;
  String? nameUz;
  String? nameEn;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['name_ru'] = nameRu;
    map['name_uz'] = nameUz;
    map['name_en'] = nameEn;
    return map;
  }
}
